package Pruebas;

public class CalculadoraBasica {
    int suma;
    int resta;
    int multiplicacion;
    int division;
    public int suma(int a, int b){
        return a + b;
    }

    public int resta (int a, int b){
        return a - b;
    }
    public int multiplicacion (int a, int b){
        return a*b;
    }
    public int division (int a, int b){
        return a/b;
    }
}
