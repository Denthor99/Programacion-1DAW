public class EjerNMultiplos {
    final static Integer N=5;
    final static Integer NUM=10;
    public static void main(String[] args) {
        // Imprimir los n primeros multiplos de un numero dado (num)
        for (int i=1;i<=N;i++){
            System.out.println("El resultado es: " + NUM*i);
        }
    }
}